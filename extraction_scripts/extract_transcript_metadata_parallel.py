FIELDNAMES = [
    "path",
    "year_from_path",
    "no_speech",
    "years",
    "months",
    "dates",
    "times",
    "locations",
    "speakers",
    "matrilines",
    "calls",
]

def process_file_task(args):
    """Wrapper for parallel execution."""
    filepath, year_from_path, root = args
    rel_path = str(filepath.relative_to(root))
    
    try:
        text = filepath.read_text(encoding="utf-8", errors="replace")
        no_speech = is_no_speech(text)

        if no_speech:
            return {
                "path": rel_path,
                "year_from_path": year_from_path,
                "no_speech": True,
                "years": "", "months": "", "dates": "", "times": "",
                "locations": "", "speakers": "", "matrilines": "", "calls": ""
            }

        return {
            "path": rel_path,
            "year_from_path": year_from_path,
            "no_speech": False,
            "years": "; ".join(str(y) for y in extract_year(text)),
            "months": "; ".join(str(m) for m in extract_month(text)),
            "dates": "; ".join(extract_date(text)),
            "times": "; ".join(extract_time(text)),
            "locations": "; ".join(extract_location(text)),
            "speakers": "; ".join(extract_speaker(text)),
            "matrilines": "; ".join(extract_matrilines(text)),
            "calls": "; ".join(extract_calls(text)),
        }
    except Exception:
        # User requirement: If error, write "error" for all columns
        return {
            "path": rel_path,
            "year_from_path": year_from_path,
            "no_speech": "error",
            "years": "error",
            "months": "error",
            "dates": "error",
            "times": "error",
            "locations": "error",
            "speakers": "error",
            "matrilines": "error",
            "calls": "error",
        }

def run_parallel(root: Path, output: Path, workers: int = 8) -> None:
    print(f"Gathering files from {root}...")
    tasks = [(fp, yl, root) for fp, yl in iter_transcripts(root)]
    total = len(tasks)
    print(f"Found {total} files. Processing using {workers} workers...")

    rows = []
    no_speech_count = 0
    error_count = 0

    with ProcessPoolExecutor(max_workers=workers) as executor:
        futures = [executor.submit(process_file_task, t) for t in tasks]
        
        for i, future in enumerate(as_completed(futures), 1):
            row = future.result()
            rows.append(row)
            
            if row["no_speech"] == "error":
                error_count += 1
            elif row["no_speech"] is True:
                no_speech_count += 1
            
            if i % 100 == 0:
                print(f"  … processed {i}/{total} files")

    # Sort rows by path to maintain consistent output
    rows.sort(key=lambda x: x["path"])

    with output.open("w", newline="", encoding="utf-8") as f:
        writer = csv.DictWriter(f, fieldnames=FIELDNAMES)
        writer.writeheader()
        writer.writerows(rows)

    print(f"\nDone. {len(rows)} files → {output}")
    print(f"  No-speech files: {no_speech_count}")
    print(f"  Errors encountered: {error_count}")
    print(f"  Valid entries:   {len(rows) - no_speech_count - error_count}")

