def extract_year(text: str) -> list[int]:
    """Return all four-digit years that look like calendar years (1970-2030)."""
    hits = re.findall(r'\b(19[7-9]\d|20[0-2]\d)\b', text)
    return sorted(set(int(y) for y in hits))


def extract_month(text: str) -> list[int]:
    """Return month numbers (1-12) found anywhere in the text."""
    pattern = rf'\b({_month_regex()})\b'
    hits = re.findall(pattern, text, flags=re.IGNORECASE)
    return sorted(set(MONTH_MAP[h.lower()] for h in hits))


def extract_date(text: str) -> list[str]:
    """
    Return date strings in normalised ISO-like form 'YYYY-MM-DD' where
    enough context is available, otherwise 'DD <MonthName>' or just the
    day number.  Returns all unique dates found.

    Handles patterns such as:
      • "Tuesday, the 26th of February, 1985"
      • "11.08 a.m. Sunday, the 2nd of June, 1985"
      • "June 24th"
      • "24th of June, 1985"
      • "6th of June, 1984"
      • "Monday, 1st of July, 1985"
    """
    dates: list[str] = []
    month_re = _month_regex()

    # Pattern A: "the 26th of February, 1985"  /  "1st of July, 1985"
    pat_a = (
        rf'\b(\d{{1,2}})(?:st|nd|rd|th)?\s+of\s+({month_re})'
        rf'(?:[,\s]+(\b(?:19[7-9]\d|20[0-2]\d)\b))?'
    )
    for m in re.finditer(pat_a, text, re.IGNORECASE):
        day, mon, yr = m.group(1), m.group(2), m.group(3)
        dates.append(_fmt_date(day, mon, yr))

    # Pattern B: "June 24th"  /  "June 24th, 1985"  /  "June 2nd"
    pat_b = (
        rf'\b({month_re})\s+(\d{{1,2}})(?:st|nd|rd|th)?'
        rf'(?:[,\s]+(\b(?:19[7-9]\d|20[0-2]\d)\b))?'
    )
    for m in re.finditer(pat_b, text, re.IGNORECASE):
        mon, day, yr = m.group(1), m.group(2), m.group(3)
        dates.append(_fmt_date(day, mon, yr))

    # Deduplicate, preserve order
    seen: set[str] = set()
    out: list[str] = []
    for d in dates:
        if d not in seen:
            seen.add(d)
            out.append(d)
    return out


def _fmt_date(day: str, month_name: str, year: str | None) -> str:
    """Format a parsed date as YYYY-MM-DD if year known, else MM-DD."""
    m = MONTH_MAP.get(month_name.lower(), 0)
    if year:
        return f"{year}-{m:02d}-{int(day):02d}"
    return f"{m:02d}-{int(day):02d}"


def extract_time(text: str) -> list[str]:
    """
    Return time strings in HH:MM (24h) format.

    Handles:
      • "6.30"  /  "6:30"  /  "6.30 a.m."  /  "6:30 p.m."
      • "19.23" / "1327"  (24-hour, with or without separator)
      • "11.08 a.m." → "11:08"
    """
    times: list[str] = []

    # HH.MM or HH:MM optionally followed by am/pm
    pat1 = r'\b(\d{1,2})[.:](\d{2})\s*(a\.?m\.?|p\.?m\.?)?\b'
    for m in re.finditer(pat1, text, re.IGNORECASE):
        h, mn, ampm = int(m.group(1)), int(m.group(2)), (m.group(3) or "")
        h = _to_24h(h, mn, ampm)
        if 0 <= h <= 23 and 0 <= mn <= 59:
            times.append(f"{h:02d}:{mn:02d}")

    # Four-digit run like "1327" (but NOT years – filtered by range)
    pat2 = r'\b(\d{4})\b'
    for m in re.finditer(pat2, text):
        val = m.group(1)
        h, mn = int(val[:2]), int(val[2:])
        # Exclude year-like values
        if 0 <= h <= 23 and 0 <= mn <= 59 and not (1900 <= int(val) <= 2030):
            times.append(f"{h:02d}:{mn:02d}")

    seen: set[str] = set()
    out: list[str] = []
    for t in times:
        if t not in seen:
            seen.add(t)
            out.append(t)
    return out


def _to_24h(h: int, mn: int, ampm: str) -> int:
    ampm = ampm.replace(".", "").lower()
    if ampm == "pm" and h != 12:
        h += 12
    elif ampm == "am" and h == 12:
        h = 0
    return h


def extract_location(text: str) -> list[str]:
    """Return canonical location names found in the text."""
    found_canonical = set()
    
    # Sort by length descending to match "Blackney Pass" before "Blackney"
    sorted_variants = sorted(LOCATION_MAP.keys(), key=len, reverse=True)
    
    for variant in sorted_variants:
        pattern = rf'\b{re.escape(variant)}\b'
        if re.search(pattern, text, re.IGNORECASE):
            # Map the found variant back to its canonical name
            found_canonical.add(LOCATION_MAP[variant])
            
    return list(found_canonical)


def extract_speaker(text: str) -> list[str]:
    """
    Return speaker names that appear to be actively speaking or are
    named as making an observation/statement.

    Strategy: look for names followed by speech-verb indicators, OR
    names that appear at the start of a turn, OR names in the known list
    that appear in the text at all (looser fallback).
    """
    found: list[str] = []
    for name in KNOWN_SPEAKERS:
        pattern = rf'\b{re.escape(name)}\b'
        if re.search(pattern, text, re.IGNORECASE):
            found.append(name)
    return found


def extract_matrilines(text: str) -> list[str]:
    """
    Return orca ID / matriline references found in text.

    Matches:
      • Bare IDs like A6, A-30, B1
      • Possessive / plural: "the A5s", "A2 pod", "A pod", "A2 gang"
      • Named whales that map to IDs: "Nicola" (A30), etc.
    """
    found: set[str] = set()

    # Direct ID matches (A6, A-30, A30 etc.)
    id_pat = r'\b([A-C]-?\d{1,3})\b'
    for m in re.finditer(id_pat, text):
        # Normalise: remove hyphen
        raw = m.group(1).replace("-", "")
        found.add(raw)

    # "A pod", "A2 pod", "A pod's"
    pod_pat = r'\b([A-C]\d?)\s+(?:pod|gang|group)\b'
    for m in re.finditer(pod_pat, text, re.IGNORECASE):
        found.add(m.group(1).upper())

    # "the A5s" / "the A2s"
    plural_pat = r'\bthe\s+([A-C]\d{1,3})s\b'
    for m in re.finditer(plural_pat, text, re.IGNORECASE):
        found.add(m.group(1).upper())

    # Named individuals → canonical ID mapping
    # Add more as needed based on your catalogue
    name_to_id: dict[str, str] = {
        "Nicola":  "A30",
        "Nickel":  "A30",   # apparent nickname in transcripts
    }
    for name, cid in name_to_id.items():
        if re.search(rf'\b{re.escape(name)}\b', text, re.IGNORECASE):
            found.add(cid)

    # Filter to only IDs that are in the canonical list (if list is non-empty)
    if KNOWN_MATRILINES:
        found = {f for f in found if f in KNOWN_MATRILINES}

    return sorted(found)


def extract_calls(text: str) -> list[str]:
    """
    Return acoustic call designations found in text (e.g. N5, S16, N7s).
    Falls back to regex if KNOWN_CALLS list is empty.
    """
    found: set[str] = set()

    if KNOWN_CALLS:
        for call in KNOWN_CALLS:
            if re.search(rf'\b{re.escape(call)}\b', text, re.IGNORECASE):
                found.add(call)
    else:
        # Generic pattern: letter + 1-3 digits, optionally trailing 's'
        call_pat = r'\b([A-Z]\d{1,3}s?)\b'
        candidates = re.findall(call_pat, text)
        # Exclude things that look like matriline IDs
        matriline_pat = re.compile(r'^[A-C]\d{1,3}s?$')
        for c in candidates:
            if not matriline_pat.match(c):
                found.add(c)

    return sorted(found)


