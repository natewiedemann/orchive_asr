def iter_transcripts(root: Path):
    """Yields (filepath, year_label) for .txt files."""
    for year_dir in sorted(root.iterdir()):
        if not year_dir.is_dir():
            continue
        year_label = year_dir.name
        transcripts_dir = year_dir / "transcripts"
        if not transcripts_dir.is_dir():
            continue
        for txt_file in sorted(transcripts_dir.glob("*.txt")):
            yield txt_file, year_label

